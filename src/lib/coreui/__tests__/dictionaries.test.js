describe("shell dictionaries", () => {
  it.each(SUPPORTED_DICTIONARY_LOCALES)(
    "provides every shell key for %s",
    (locale) => {
      const dictionary = getDictionary(locale);

      for (const key of SHELL_DICTIONARY_KEYS) {
        expect(dictionary[key], `${locale}:${key}`).toBeTruthy();
      }
    }
  );

  it("repairs mojibake in localized shell copy", () => {
    for (const locale of SUPPORTED_DICTIONARY_LOCALES.filter((value) => value !== "en")) {
      const dictionary = getDictionary(locale);

      for (const key of SHELL_DICTIONARY_KEYS) {
        expect(dictionary[key], `${locale}:${key}`).not.toMatch(/[ÃÂâ]/);
      }
    }
  });
});
